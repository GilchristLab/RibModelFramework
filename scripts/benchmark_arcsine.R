suppressPackageStartupMessages(library(AnaCoDa))

args <- commandArgs(trailingOnly = TRUE)

fasta_1k <- file.path(dirname(dirname(dirname(
              tryCatch(normalizePath(sys.frame(0)$ofile), error=function(e) getwd())))),
              "tests/testthat/UnitTestingData/testMCMCROCFiles/simulatedAllUniqueR.fasta")
if (!file.exists(fasta_1k))
  fasta_1k <- "tests/testthat/UnitTestingData/testMCMCROCFiles/simulatedAllUniqueR.fasta"

# Build a 2000-gene FASTA in /tmp if no file was supplied
default_fasta <- "/tmp/simROC_2000.fasta"
if (!file.exists(default_fasta) && file.exists(fasta_1k)) {
  lines <- readLines(fasta_1k)
  lines_b <- ifelse(startsWith(lines, ">"),
                    paste0(sub("^>", ">b_", lines)), lines)
  writeLines(c(lines, lines_b), default_fasta)
}

genome_file <- if (length(args) >= 1) args[1] else default_fasta
samples     <- if (length(args) >= 2) as.integer(args[2]) else 500L
thinning    <- if (length(args) >= 3) as.integer(args[3]) else 10L
n_iter      <- samples * thinning
adaptive_w  <- max(10L, as.integer(samples / 10L))

cat("============================================================\n")
cat("ROC Arcsine Approximation Benchmark -- Single-LN Fit\n")
cat("============================================================\n")
cat(sprintf("Genome:        %s\n", genome_file))
cat(sprintf("MCMC:          samples=%d  thinning=%d  (%d iterations)\n",
            samples, thinning, n_iter))
cat(sprintf("adaptive.width %d\n\n", adaptive_w))

# ------------------------------------------------------------------
# Load genome
# ------------------------------------------------------------------
genome <- initializeGenomeObject(file = genome_file)
G      <- length(genome)
cat(sprintf("Genome size:   G = %d genes\n\n", G))

# ------------------------------------------------------------------
# Helper: run one timed MCMC replicate
# ------------------------------------------------------------------
run_one <- function(label, approx_val, seed = 42L) {
  set.seed(seed)
  gene_assign <- rep(1L, G)
  p  <- initializeParameterObject(genome = genome, sphi = 1.0,
                                   num.mixtures = 1L,
                                   gene.assignment = gene_assign)
  m  <- initializeModelObject(p, "ROC", approx = approx_val)
  mc <- initializeMCMCObject(samples = samples, thinning = thinning,
                              adaptive.width = adaptive_w,
                              est.expression = TRUE, est.csp = TRUE,
                              est.hyper = TRUE)
  # suppress per-iteration stdout
  cap <- textConnection("mcmc_out", "w")
  sink(cap)
  elapsed <- system.time(
    runMCMC(mcmc = mc, genome = genome, model = m, ncores = 1L)
  )
  sink(); close(cap)

  list(label   = label,
       model   = m,
       mcmc    = mc,
       elapsed = elapsed)
}

# ------------------------------------------------------------------
# Run both methods
# ------------------------------------------------------------------
cat("--- Running exact multinomial ---\n")
res_e <- run_one("exact",   approx_val = FALSE)
cat(sprintf("    done in %.1f s\n\n", res_e$elapsed["elapsed"]))

cat("--- Running hybrid arcsine ---\n")
res_a <- run_one("arcsine", approx_val = TRUE)
cat(sprintf("    done in %.1f s\n\n", res_a$elapsed["elapsed"]))

# ------------------------------------------------------------------
# Timing report
# ------------------------------------------------------------------
t_e <- res_e$elapsed["elapsed"]
t_a <- res_a$elapsed["elapsed"]

cat("============================================================\n")
cat("TIMING\n")
cat("============================================================\n")
cat(sprintf("  Exact:   %6.1f s   (%5.2f ms / iteration)\n",
            t_e, 1000 * t_e / n_iter))
cat(sprintf("  Arcsine: %6.1f s   (%5.2f ms / iteration)\n",
            t_a, 1000 * t_a / n_iter))
cat(sprintf("  Ratio:   %.3fx  (%s)\n\n",
            t_e / t_a,
            if (t_e > t_a) "arcsine faster" else "exact faster"))

# ------------------------------------------------------------------
# LL trace comparison
# ------------------------------------------------------------------
ll_e <- res_e$mcmc$getLogLikelihoodTrace()
ll_a <- res_a$mcmc$getLogLikelihoodTrace()

burn  <- max(1L, as.integer(length(ll_e) * 0.2))   # 20% burnin
ll_e_post <- ll_e[burn:length(ll_e)]
ll_a_post <- ll_a[burn:length(ll_a)]

cat("============================================================\n")
cat("LOG-LIKELIHOOD TRACES (post-burnin)\n")
cat("============================================================\n")
cat(sprintf("  Exact   mean=%.1f  sd=%.2f  range=[%.1f, %.1f]\n",
            mean(ll_e_post), sd(ll_e_post), min(ll_e_post), max(ll_e_post)))
cat(sprintf("  Arcsine mean=%.1f  sd=%.2f  range=[%.1f, %.1f]\n\n",
            mean(ll_a_post), sd(ll_a_post), min(ll_a_post), max(ll_a_post)))
cat("  Note: arcsine LL is on a different scale than exact (different\n")
cat("  curvature / missing normalising constant), so absolute values\n")
cat("  are not directly comparable.\n\n")

# ------------------------------------------------------------------
# sphi (stdDevSynthesisRate) recovery
# ------------------------------------------------------------------
# Trace is stored in the parameter object, not the MCMC object.
# getStdDevSynthesisRateTraces() returns a matrix (rows = samples,
# cols = selection categories); take column 1 for single mixture.
# getStdDevSynthesisRateTraces() returns a matrix (samples x categories)
# or sometimes a vector for single-mixture; as.numeric() normalises both.
# getStdDevSynthesisRateTraces() returns a list (one element per selection
# category).  For a single-mixture fit, take element [[1]].
sphi_e <- res_e$model$getParameter()$getTraceObject()$getStdDevSynthesisRateTraces()[[1]]
sphi_a <- res_a$model$getParameter()$getTraceObject()$getStdDevSynthesisRateTraces()[[1]]

sphi_e_post <- sphi_e[burn:length(sphi_e)]
sphi_a_post <- sphi_a[burn:length(sphi_a)]

cat("============================================================\n")
cat("SPHI (stdDevSynthesisRate) POSTERIOR\n")
cat("============================================================\n")
cat(sprintf("  Exact   mean=%.4f  sd=%.4f  95%% CI=[%.4f, %.4f]\n",
            mean(sphi_e_post), sd(sphi_e_post),
            quantile(sphi_e_post, 0.025), quantile(sphi_e_post, 0.975)))
cat(sprintf("  Arcsine mean=%.4f  sd=%.4f  95%% CI=[%.4f, %.4f]\n\n",
            mean(sphi_a_post), sd(sphi_a_post),
            quantile(sphi_a_post, 0.025), quantile(sphi_a_post, 0.975)))

# ------------------------------------------------------------------
# Acceptance rate proxy: fraction of LL trace that changed
# ------------------------------------------------------------------
acc_e <- mean(diff(ll_e) != 0)
acc_a <- mean(diff(ll_a) != 0)
cat("============================================================\n")
cat("APPROXIMATE ACCEPTANCE RATE (LL changed between samples)\n")
cat("============================================================\n")
cat(sprintf("  Exact:   %.3f\n", acc_e))
cat(sprintf("  Arcsine: %.3f\n\n", acc_a))

cat("============================================================\n")
cat("INTERPRETATION NOTES\n")
cat("============================================================\n")
cat("  * Per-iteration speed: arcsine adds exp/sqrt/asin operations\n")
cat("    on top of the same calculateLogCodonProbabilityVector call,\n")
cat("    so it is unlikely to be faster in standard MH-MCMC.\n")
cat("  * Potential benefit: arcsine produces a Gaussian likelihood\n")
cat("    surface that could enable gradient-based samplers (HMC/NUTS)\n")
cat("    or Laplace/variational approximations.\n")
cat("  * Quality check: if sphi posteriors agree, the approximation\n")
cat("    is recovering the same signal despite the curvature mismatch.\n")
cat("============================================================\n")
