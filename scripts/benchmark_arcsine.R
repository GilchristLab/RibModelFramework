# Usage:        Rscript benchmark_arcsine.R [genome.fasta [samples [thinning]]]
# Input:        FASTA genome file (default: simulated 2000-gene test genome)
# Output:       Stdout timing/quality report; optional PDF trace plots
#
# Options:
#   arg1  Path to genome FASTA (default: uses simulatedAllUniqueR x2)
#   arg2  MCMC samples        (default: 500)
#   arg3  MCMC thinning       (default: 10)
#
# Requirements:
#   - AnaCoDa package installed
#   - G >= 100 genes recommended for stable timing
#
# Session:      normal-approx
# AI Model:     Claude Sonnet 4.6
# Attribution:  Generated by Michael Gilchrist in collaboration with
#               ClaudeAI Sonnet 4.6
